    public ArrayList<bidder> userList() {
        ArrayList<bidder> usersList = new ArrayList<>();
        try {
            String sql = "SELECT * FROM `bidding`";
            st = con.createStatement();
            rs = st.executeQuery(sql);

            bidder bdr;

            while (rs.next()) {
                bdr = new bidder(rs.getString("Customer"), rs.getInt("Bid"));
                usersList.add(bdr);

            }
        } catch (Exception ex) {
            Logger.getLogger(showCustomers.class.getName()).log(Level.SEVERE, null, ex);
        }
        return usersList;
    }

    public void userTable() {
        ArrayList<bidder> list = userList();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] row = new Object[2];

        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getName();
            row[1] = list.get(i).getBid();

            //model.rowsRemoved();
            model.addRow(row);
        }
        jTable1.setRowHeight(45);
    }

    Timer timer = new Timer();
    TimerTask task = new TimerTask() {
        @Override
        public void run() {
            sec++;
            if (sec < 10) {
                sec_lbl.setText("0" + String.valueOf(sec));
            } else {
                sec_lbl.setText(String.valueOf(sec));
            }

            if (min < 10) {
                min_lbl.setText("0" + String.valueOf(min));
            } else {
                min_lbl.setText(String.valueOf(min));
            }

            if (hour < 10) {
                hour_lbl.setText("0" + String.valueOf(hour));
            } else {
                hour_lbl.setText(String.valueOf(hour));
            }

            if (sec == 59) {
                sec = -1;
                min++;
            }
            if (min == 60) {
                min = 00;
                hour++;
            }
            if (hour == getHour && min == getMin && sec == 0 && getAm.equals(am_pm)) {
                flag = 1;
            }
            if (am_pm.equals(getAm) && hour >= getHour + 6) {
                flag = -1;
            } else {
                flag = 1;
            }

            if (flag == 1) {
                temp = hour;
                bid.setEnabled(true);
                ending.setText("Ends in ->");
                endHour.setText(String.valueOf(Math.abs(getHour + 6 - hour)));
                //JOptionPane.showMessageDialog(rootPane, "Auction is started. You can start Bidding now.", "Auction Started", JOptionPane.WARNING_MESSAGE);
                //flag = 0;
                if (hour == temp + 6 && tempMin == getMin) {
                    bid.setEnabled(false);
                    flag = -1;
                    endHour.setText(String.valueOf(Math.abs(getHour + 6 - hour)));

                }
            }

            if (flag == -1) {
                bid.setEnabled(false);
                ending.setText("Duration:");
                endHour.setText("06");
            }

        }
    };

