    private void bidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bidActionPerformed

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for (int i = model.getRowCount() - 1; i >= 0; i--) {
            model.removeRow(i);
        }

        String query = "INSERT INTO `bidding`(`Customer`, `Bid`) VALUES (?, ?)";

        try {
            ps = con.prepareStatement(query);
            ps.setString(1, name);
            ps.setInt(2, Integer.parseInt(price.getText()));

            ps.execute();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        userTable();

        if (flag == 1) {
            try {
                Statement stm;
                ResultSet rslt;
                String max = "SELECT Customer, MAX(Bid) FROM bidding";

                stm = con.createStatement();
                rslt = stm.executeQuery(max);

                if (rslt.next()) {
                    Cmax1.setText(rslt.getString("Customer"));
                    Cmax.setText(String.valueOf(rslt.getInt("MAX(Bid)")));
                }
            } catch (SQLException ex) {
                Logger.getLogger(purchase.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bidActionPerformed

