    private void startTime() {
        Connection conn = SqlConnect.ConnectDB();
        try {
            String sqlq = "SELECT * FROM `starttime`";
            Statement stmt = conn.createStatement();
            ResultSet rst = stmt.executeQuery(sqlq);

            while (rst.next()) {
                getHour = rst.getInt("Hour");
                getMin = rst.getInt("Minute");
                getAm = rst.getString("am_pm");
                if (getHour < 10) {
                    hour_lbl1.setText("0" + getHour);
                } else {
                    hour_lbl1.setText(String.valueOf(getHour));
                }
                if (getMin < 10) {
                    min_lbl1.setText("0" + getMin);
                } else {
                    min_lbl1.setText(String.valueOf(getMin));
                }
                sec_lbl1.setText(rst.getString("am_pm"));
            }
            rst.close();
        } catch (SQLException ex) {
            Logger.getLogger(purchase.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }
            } catch (Exception e) {
            }
        }
    }

